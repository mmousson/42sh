int		is_in_brac(int pos, char *str)
{
	int		k;

	k = pos;
	if (pos == 0 || str[pos - 1] != '[')
		return (0);
	while (str[++k])
	{
		if (str[k] == ']')
			return (1);
	}
	return (0);
}
