#include "glob.h"

void	walk_word(char **s)
{
	while (**s)
	{
		if (**s == '[' || **s == '*' || **s == '?' || **s == '/')
			break ;
		if (**s == '\\')
			++*s;
		else if (is_quote(**s))
			walk_quote(s);
		if (**s)
			++*s;
	}
}
